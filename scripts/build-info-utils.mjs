import { execFileSync } from "node:child_process";
import fs from "node:fs/promises";
import path from "node:path";

const AUTO_COMMIT_MESSAGE_PREFIX = "build: prepare source snapshot for";

function resolveGitExecutable() {
  const envGit = process.env.GIT_EXE?.trim();
  if (envGit) {
    return envGit;
  }
  try {
    const output = execFileSync("where.exe", ["git"], {
      encoding: "utf8",
      stdio: ["ignore", "pipe", "pipe"]
    }).trim();
    const gitPath = output
      .split(/\r?\n/)
      .map((line) => line.trim())
      .find(Boolean);
    if (gitPath) {
      return gitPath;
    }
  } catch {
    // fall through to default binary name
  }
  return "git";
}

function runGit(args, cwd) {
  return execFileSync(resolveGitExecutable(), args, {
    cwd,
    encoding: "utf8",
    stdio: ["ignore", "pipe", "pipe"]
  });
}

function toErrorMessage(error) {
  if (!error || typeof error !== "object") {
    return String(error);
  }
  const stderr = "stderr" in error && typeof error.stderr === "string" ? error.stderr.trim() : "";
  if (stderr) {
    return stderr;
  }
  if ("message" in error && typeof error.message === "string") {
    return error.message;
  }
  return String(error);
}

function splitGitOutputLines(output) {
  return output
    .split(/\r?\n/)
    .map((line) => line.trim())
    .filter(Boolean);
}

function readGitPathList(args, cwd, context) {
  try {
    return splitGitOutputLines(runGit(args, cwd));
  } catch (error) {
    throw new Error(`Failed to ${context}: ${toErrorMessage(error)}`);
  }
}

function uniqueSorted(values) {
  return [...new Set(values)].sort((left, right) => left.localeCompare(right));
}

export function listDirtyGitPaths(cwd) {
  return uniqueSorted([
    ...readGitPathList(["diff", "--name-only", "--cached", "--"], cwd, "read staged git changes"),
    ...readGitPathList(["diff", "--name-only", "--"], cwd, "read unstaged git changes"),
    ...readGitPathList(
      ["ls-files", "--others", "--exclude-standard", "--"],
      cwd,
      "read untracked git changes"
    )
  ]);
}

export function buildAutoCommitMessage(displayVersion) {
  return {
    title: `${AUTO_COMMIT_MESSAGE_PREFIX} ${displayVersion}`,
    body: "Auto-generated by tauri:build so the packaged app and the pushed git revision use the same committed source snapshot."
  };
}

export function stageGitPaths(cwd, paths) {
  if (paths.length === 0) {
    return;
  }
  try {
    // Stage all pending changes so ignored build outputs do not fail the build.
    runGit(["add", "-A"], cwd);
  } catch (error) {
    throw new Error(`Failed to stage git changes: ${toErrorMessage(error)}`);
  }
}

export function createGitCommit(cwd, message) {
  const title = typeof message?.title === "string" ? message.title.trim() : "";
  const body = typeof message?.body === "string" ? message.body.trim() : "";
  if (!title) {
    throw new Error("Auto commit title must not be empty.");
  }
  const args = ["commit", "-m", title];
  if (body) {
    args.push("-m", body);
  }
  try {
    runGit(args, cwd);
  } catch (error) {
    throw new Error(`Failed to create git commit: ${toErrorMessage(error)}`);
  }
}

function getGitCurrentBranch(cwd) {
  try {
    const branch = runGit(["branch", "--show-current"], cwd).trim();
    if (!branch) {
      throw new Error("Current branch name is empty.");
    }
    return branch;
  } catch (error) {
    throw new Error(`Failed to read current git branch: ${toErrorMessage(error)}`);
  }
}

function getGitUpstreamBranch(cwd) {
  try {
    const upstream = runGit(
      ["rev-parse", "--abbrev-ref", "--symbolic-full-name", "@{u}"],
      cwd
    ).trim();
    return upstream || null;
  } catch {
    return null;
  }
}

function listGitRemotes(cwd) {
  try {
    return splitGitOutputLines(runGit(["remote"], cwd));
  } catch (error) {
    throw new Error(`Failed to read git remotes: ${toErrorMessage(error)}`);
  }
}

export function resolveGitPushArgs({ branchName, upstreamBranch, remotes }) {
  if (upstreamBranch) {
    return ["push"];
  }
  if (!branchName) {
    throw new Error("Cannot push because the current branch name is empty.");
  }
  if (remotes.includes("origin")) {
    return ["push", "-u", "origin", branchName];
  }
  if (remotes.length === 1) {
    return ["push", "-u", remotes[0], branchName];
  }
  if (remotes.length === 0) {
    throw new Error("Cannot push because no git remote is configured.");
  }
  throw new Error(
    `Cannot choose a git remote automatically for branch ${branchName}. Available remotes: ${remotes.join(", ")}`
  );
}

export function pushCurrentGitBranch(cwd) {
  const branchName = getGitCurrentBranch(cwd);
  const upstreamBranch = getGitUpstreamBranch(cwd);
  const remotes = listGitRemotes(cwd);
  const args = resolveGitPushArgs({ branchName, upstreamBranch, remotes });
  try {
    runGit(args, cwd);
  } catch (error) {
    throw new Error(`Failed to push git branch: ${toErrorMessage(error)}`);
  }
  return {
    branchName,
    upstreamBranch,
    pushArgs: args
  };
}

export function assertCleanGitWorktree(cwd) {
  const dirtyPaths = listDirtyGitPaths(cwd);
  if (dirtyPaths.length > 0) {
    throw new Error(
      `Git worktree is dirty. Please commit or stash all changes before build. Pending paths: ${dirtyPaths.join(", ")}`
    );
  }
}

export function getGitShortHash(cwd) {
  let hash = "";
  try {
    hash = runGit(["rev-parse", "--short", "HEAD"], cwd).trim();
  } catch {
    throw new Error("Git HEAD not found. Please create at least one commit before build.");
  }

  if (!hash) {
    throw new Error("Failed to read git short hash.");
  }
  return hash;
}

export function getGitUpdateNotes(cwd, previousGitHash) {
  const fromHash = typeof previousGitHash === "string" ? previousGitHash.trim() : "";
  try {
    const args = fromHash
      ? ["log", "--format=%h %s", `${fromHash}..HEAD`]
      : ["log", "--format=%h %s", "HEAD"];
    const output = runGit(args, cwd).trim();
    if (!output) {
      return ["No git commits since previous version."];
    }
    return output
      .split(/\r?\n/)
      .map((line) => line.trim())
      .filter(Boolean);
  } catch (error) {
    throw new Error(`Failed to read git update notes: ${toErrorMessage(error)}`);
  }
}

function toGeneratedModuleContent(payload) {
  return `export const BUILD_INFO = ${JSON.stringify(payload, null, 2)};\n`;
}

export async function writeGeneratedBuildInfo(root, payload) {
  const generatedDir = path.join(root, "src", "generated");
  const targetPath = path.join(generatedDir, "build-info.ts");
  await fs.mkdir(generatedDir, { recursive: true });
  await fs.writeFile(targetPath, toGeneratedModuleContent(payload), "utf8");
  return targetPath;
}

export async function readBuildHistory(historyPath) {
  try {
    const raw = await fs.readFile(historyPath, "utf8");
    const parsed = JSON.parse(raw);
    if (!parsed || typeof parsed !== "object" || !Array.isArray(parsed.entries)) {
      throw new Error(`Invalid build history format in ${historyPath}`);
    }
    return parsed.entries;
  } catch (error) {
    if (error && typeof error === "object" && "code" in error && error.code === "ENOENT") {
      return [];
    }
    throw error;
  }
}

function sortHistoryEntries(entries) {
  return [...entries].sort((left, right) => {
    const leftTime = Date.parse(left.updatedAt ?? "");
    const rightTime = Date.parse(right.updatedAt ?? "");
    return rightTime - leftTime;
  });
}

export async function writeBuildHistory(historyPath, entry) {
  const history = await readBuildHistory(historyPath);
  const deduped = history.filter(
    (item) => !(item.displayVersion === entry.displayVersion && item.gitHash === entry.gitHash)
  );
  const entries = sortHistoryEntries([entry, ...deduped]);
  await fs.mkdir(path.dirname(historyPath), { recursive: true });
  await fs.writeFile(historyPath, `${JSON.stringify({ entries }, null, 2)}\n`, "utf8");
  return entries;
}

export function buildBuildInfoPayload({
  versionInfo,
  gitHash,
  previousGitHash,
  updateNotes,
  artifactPath,
  updatedAt,
  historyEntries,
  historyFilePath
}) {
  return {
    displayVersion: versionInfo.display,
    semverVersion: versionInfo.semver,
    gitHash,
    previousGitHash,
    updateNotes,
    artifactPath,
    updatedAt,
    historyFilePath,
    historyEntries: sortHistoryEntries(historyEntries).slice(0, 20)
  };
}
